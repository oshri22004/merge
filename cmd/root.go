package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "merge.git",
	Short: "git pr, push, merge, and wait for workflows in one command",
	Long: `merge.git is a CLI tool that automates the process of creating a pull request,
	pushing the current branch, waiting for workflows to finish, and merging the pull request.`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

